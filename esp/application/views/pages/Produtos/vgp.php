			<p data-ani-type="fade-top" data-ani-delay=".2">
				<img src="media/img/o-programa.png" alt="" class="fr">
				El Instituto Via Green ha desarrollado el Programa de Gestión de Emisión de Contaminantes Atmosféricos (VGP) para el sector de transporte de cargas y pasajeros, abarcando los cuatro modos: por carretera, marítimo, ferroviario y aéreo. El VGP es un programa innovador y voluntario, que ofrece un conjunto de iniciativas para la completa gestión de las emisiones de Gases de Efecto Invernadero y otros contaminantes provenientes de las operaciones del sector de transporte de las organizaciones miembros. El programa pretende acelerar e incentivar empresas a adoptar mejores prácticas operacionales, por tanto, contribuyendo para un clima más equilibrado y una atmosfera libre de contaminantes.
